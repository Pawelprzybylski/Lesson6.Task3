public class Calculator {

    public static int addResult(int a, int b) {
        return a + b;
    }

    public static int substractResult(int a, int b) {
        return a - b;
    }

    public static int multiplyResult(int a, int b) {
        return a * b;
    }

    public static int divideResult(int a, int b) {
        return a / b;
    }

}
